package com.vibol.Datastructure.excercise;

public class Data {

	private String id;
	private Long value;

	public Data(String id, Long value) {
		super();
		this.id = id;
		this.value = value;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Long getValue() {
		return value;
	}

	public void setValue(Long value) {
		this.value = value;
	}

}
